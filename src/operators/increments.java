package operators;

public class increments {

	
	public static void main(String[] args)
	
	{
		
		int a=1;
		int b=a++;//postincrement
		
		System.out.println(a);//a=2
		System.out.println(b);//b=1
		
		
		int x=1;
		int y=++x;//preincrement
		
		System.out.println(x);//2
		System.out.println(y);//2
		
	}
}
