package loops;

public class javaloops {
	
	public static void even()
	{
		int k=2;
		do
		{
			System.out.println("even");
			k++;
		}
		while(k%2==0);
			
		}

		
		public static void main(String[] args)
		{
			int j=0;
			
		
		for(int i=0;i<8;i++)
		{
			System.out.println(i);
		}
		
		System.out.println("=============================");
		
		
		while(j<=10)
		{  
			System.out.println(j);
			j++;
		}
		System.out.println("=======================================");
		
		even();
	}
	}


