package methods;

public class Defaultvalues {
	
	static int a;
	static boolean b;
	static char c;
	static double d;

	public static void main(String[] args) {
		
		
		System.out.println(a);//0
        System.out.println(b);//false
        System.out.println(c);//blank
        System.out.println(d);//0.0
	}

}
